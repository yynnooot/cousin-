                <h2>About Tae</h2>
            </div>
            <div class="row" id="about">               
                <div id="headshot" class="col span-2-of-5 js--wp-1">
                    <img src="images/about-portrait.jpg" alt="portrait">
                </div>
                <div class="col span-3-of-5">                   
                    <p>Tae Yoon was born and raised in Flushing, Queens, and majored in writing at Eugene Lang College at The New School. Upon graduation, he began working at Food & Wine magazine, which kicked off his illustrious career in the dynamic food and restaurant industry. Since then, he's worked at renowned places such as Momofuku Ssam Bar, P.D.T., Sunny Spot, Khe-Yo, Suzume, Sempio, and Cocktail Kingdom.</p><br>

                    <p>Tae's industry expertise and dedication to writing has been chronicled in his personal blog since 2006. He's also been a contributor to VICE since 2013, in addition to writing for their food channel MUNCHIES since launching in 2014. His writing has also appeared in Gothamist and Edible Queens.</p><br>

                    <p>In the summer of 2017, Tae will be heading to Seoul in order to work on a book about the food and restaurant scene of South Korea's vibrant capital city.</p><br>

                    <p>He currently resides in New York City and is in search of literary representation, with a book proposal and samples of fiction available upon request.</p>
